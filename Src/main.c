int _write(int fd, char *ptr, int len)
{ 	
  int i = 0;
  if(fd > 2) {
    return -1;
  }
  while(*ptr && (i<len)) {
    while((USART1->SR&0X40)==0); 
    USART1->DR = (uint8_t) *ptr;
    if(*ptr == '\n') {
      while((USART1->SR&0X40)==0); 
      USART1->DR = (uint8_t) '\r';
    }
    i++;
    ptr++;
  }  
  return i;
}

